function height = elevation(x,y)

       p00 =       0;
       p10 =      0.2088;
       p01 =    -0.06782;  
       p20 =   -0.004656;  
       p11 =   0.0009188;  
       p02 =     0.02267;  
       p30 =   0.0001275; 
       p21 =   -1.22e-05;  
       p12 =    6.34e-07;  
       p03 =   0.0004269; 
       p40 =  -8.472e-07;  
       p31 =   1.502e-08;  
       p22 =  -4.967e-07  ;
       p13 =   1.413e-07 ; 
       p04 =  -2.386e-05;

height =  p00 + p10*x + p01*y + p20*x^2 + p11*x*y + p02*y^2 + p30*x^3 + p21*x^2*y + p12*x*y^2 + p03*y^3 + p40*x^4 + p31*x^3*y + p22*x^2*y^2 + p13*x*y^3 + p04*y^4;

end

